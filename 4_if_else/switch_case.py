if k == 1:
    result = "плохо"
elif k == 2:
    result = "неудовлетворительно"
elif k == 3:
    result = "удовлетворительно"
elif k == 4:
    result = "хорошо"
elif k == 5:
    result = "отлично"
else:
    result = "ошибка"

print(result)


