# 4. Робот может перемещаться в четырех направлениях («С» — север, «З» — запад, «Ю» — юг, «В» — восток)
# и принимать три цифровые команды: 0 — продолжать движение, 1 — поворот налево, −1 — поворот направо.
# Дан символ C — исходное направление робота и целое число N — посланная ему команда.
# Вывести направление робота после выполнения полученной команды.

current_direction = input('\nВведите направление: ').upper()
command = int(input('Введите команду: '))
directions = ['N', 'E', 'S', 'W']
index = directions.index(current_direction)

if command == 0:
    print('Продолжаю движение')
elif command == 1:
    index = (index - 1) % 4
    print(f'Повернули на {directions[index]}')
elif command == -1:
    index = (index + 1) % 4
    print(f'Повернули на {directions[index]}')

# 5. Дано целое число в диапазоне 100–999.
# Вывести строку-описание данного числа, например:
# 256 — «двести пятьдесят шесть», 814 — «восемьсот четырнадцать».

number = int(input('\nВведите число: '))

hundreds = {
    1: "сто", 2: "двести", 3: "триста", 4: "четыреста",
    5: "пятьсот", 6: "шестьсот", 7: "семьсот",
    8: "восемьсот", 9: "девятьсот"
}

tens = {
    2: "двадцать", 3: "тридцать", 4: "сорок",
    5: "пятьдесят", 6: "шестьдесят",
    7: "семьдесят", 8: "восемьдесят", 9: "девяносто"
}

teens = {
    10: "десять", 11: "одиннадцать", 12: "двенадцать", 13: "тринадцать",
    14: "четырнадцать", 15: "пятнадцать", 16: "шестнадцать",
    17: "семнадцать", 18: "восемнадцать", 19: "девятнадцать"
}

units = {
    1: "один", 2: "два", 3: "три", 4: "четыре", 5: "пять",
    6: "шесть", 7: "семь", 8: "восемь", 9: "девять"
}

hundred = number // 100
ten = (number % 100) // 10
unit = number % 10

result = hundreds[hundred]

if ten == 1:
    result += " " + teens[10 + unit]
else:
    if ten > 1:
        result += " " + tens[ten]
    if unit > 0:
        result += " " + units[unit]

print(result)

